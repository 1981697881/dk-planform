<template>
  <div>
    <list
      class="list-main box-shadow"
      :columns="columns"
      :loading="loading"
      :list="list"
      index
      type
      @handle-size="handleSize"
      @handle-current="handleCurrent"
      @row-click="rowClick"
    />
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import { getGoodsList, deleteCommodity } from '@/api/commodity/index'
import List from '@/components/List'

export default {
  components: {
    List
  },
  computed: {
    ...mapGetters(['node'])
  },
  data() {
    return {
      loading: false,
      visible: false,
      list: {},
      fid: null,
      type: null,
      columns: [
        { text: '产品名称', name: 'productName' },
        { text: '产品内码', name: 'productInnerCode' },
        { text: '材料分类', name: 'materialClassification' },
        { text: '形式', name: 'modality' },
        { text: '订单时间', name: 'orderDate' },
        { text: '生产订单号', name: 'orderNumber' },
        { text: '规格', name: 'specifications' },
        { text: '表面处理', name: 'surfaceTreatment' },
        { text: '品种', name: 'varieties' },
        { text: '产品压力', name: 'productPressure' },
        { text: '质检工', name: 'qualityTestingWork' },
        { text: '配托工', name: 'supportingWork' },
        { text: '打压工', name: 'suppressWork' },
        { text: '流向地区', name: 'flowArea' },
        { text: '流向单位', name: 'flowDirectionUnit' },
      ]
    }
  },

  methods: {
    ExportData() {
      import('@/vendor/Export2Excel').then(excel => {
        // 表格的表头列表
        const columns = this.columns
        const tHeader = []
        // 与表头相对应的数据里边的字段
        const filterVal = []
        columns.forEach((item, index) => {
          tHeader.push(item.text)
          filterVal.push(item.name)
        })
        const list = this.list.records
        const data = this.formatJson(filterVal, list);
        // 这里还是使用export_json_to_excel方法比较好，方便操作数据
        excel.export_json_to_excel([tHeader],data,'产品信息')
      })
    },
    formatJson(filter, jsonDate){
      return jsonDate.map(v =>
        filter.map(j => {
          return v[j]
        })
      )
    },
    // 监听每页显示几条
    handleSize(val) {
      this.list.size = val
      this.$emit('uploadList')
    },
    // 监听当前页
    handleCurrent(val) {
      this.list.current = val
      this.$emit('uploadList')
    },
    Delivery(val) {
      deleteCommodity(val).then(res => {
        if(res.flag) {
          this.$store.dispatch('list/setClickData', '');
          this.$emit('uploadList')
        }
      });
    },
    uploadPr(val) {
      this.fetchData(val, {
        pageNum: 1,
        pageSize: this.list.size || 50
      })
    },
    dblclick(obj) {
      this.$emit('showDialog', obj.row)
    },
    // 监听单击某一行
    rowClick(obj) {
      this.$store.dispatch('list/setClickData', obj.row)
    },
    fetchData(val, data = {
      pageNum: this.list.current || 1,
      pageSize: this.list.size || 50
    }) {
      this.loading = true;
      getGoodsList(data, val).then(res => {
        this.loading = false
        this.list = res.data
      })
    }
  }
}
</script>

