import request from '@/utils/request'
import {
  getToken
} from '@/utils/auth'

// 职员管理-获取列表
export function getClerkList(params, query) {
  const url = '/Admin/employee/list/' + params.pageNum + '/' + params.pageSize
  return request({
    url: url,
    headers: {
      'authorization': getToken('dkrx'),
      'Content-Type': 'application/json'
    },
    method: 'POST',
    data: query
  })
}

